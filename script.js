// --- Menu Toggle ---
function toggleMenu() {
  const menu = document.querySelector(".menu-links");
  const icon = document.querySelector(".hamburger-icon");
  menu.classList.toggle("open");
  icon.classList.toggle("open");
  // Update aria-expanded for accessibility
  const isOpen = menu.classList.contains("open");
  icon.setAttribute("aria-expanded", isOpen ? "true" : "false");
}

// --- Scroll Progress Indicator ---
(function() {
  const progressBar = document.createElement('div');
  progressBar.style.cssText = `
    position: fixed;
    top: 0;
    left: 0;
    height: 3px;
    background: linear-gradient(90deg, #b61818, #d42020);
    width: 0%;
    z-index: 9999;
    transition: width 0.1s ease;
  `;
  document.body.appendChild(progressBar);
  
  window.addEventListener('scroll', () => {
    const scrollTop = window.pageYOffset;
    const docHeight = document.documentElement.scrollHeight - window.innerHeight;
    const scrollPercent = (scrollTop / docHeight) * 100;
    progressBar.style.width = scrollPercent + '%';
  });
})();

// --- Nav Dark/Light Toggle on Scroll ---
(function() {
  const desktopNav = document.getElementById('desktop-nav');
  const hamburgerNav = document.getElementById('hamburger-nav');
  const hero = document.getElementById('profile');
  if (!hero) return;

  function updateNav() {
    const heroBottom = hero.offsetTop + hero.offsetHeight;
    const scrolled = window.scrollY > heroBottom - 80;
    if (desktopNav) desktopNav.classList.toggle('nav-scrolled', scrolled);
    if (hamburgerNav) hamburgerNav.classList.toggle('nav-scrolled', scrolled);
  }

  window.addEventListener('scroll', updateNav, { passive: true });
  updateNav();
})();

// --- Active Nav Section Tracking ---
(function() {
  const sections = document.querySelectorAll('section[id]');
  const navLinks = document.querySelectorAll('#desktop-nav .nav-links a');

  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        const id = entry.target.getAttribute('id');
        navLinks.forEach(link => {
          link.classList.toggle('nav-active', link.getAttribute('href') === '#' + id);
        });
      }
    });
  }, { threshold: 0.3, rootMargin: '-20% 0px -50% 0px' });

  sections.forEach(section => observer.observe(section));
})();

// --- Intersection Observer for Card & Section Animations ---
(function() {
  const observerOptions = {
    threshold: 0.15,
    rootMargin: '0px 0px -60px 0px'
  };
  
  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('animate-in');
      }
    });
  }, observerOptions);
  
  document.querySelectorAll('.service-card, .project-card-future, .details-container, .au-pillar, .hww-step').forEach(card => {
    observer.observe(card);
  });

  // Section header reveals
  const revealObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) entry.target.classList.add('visible');
    });
  }, { threshold: 0.1, rootMargin: '0px 0px -40px 0px' });

  document.querySelectorAll('.section-reveal').forEach(el => revealObserver.observe(el));
})();

// --- Smooth Section Navigation ---
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
  anchor.addEventListener('click', function (e) {
    e.preventDefault();
    const target = document.querySelector(this.getAttribute('href'));
    if (target) {
      target.scrollIntoView({
        behavior: 'smooth',
        block: 'start'
      });
    }
  });
});

